import { gql } from '@apollo/client';

export const LOGIN = gql`
  mutation login($email: String!, $password: String!) {
    login(email: $email, password: $password) {
      token
      user {
        _id
      }
    }
  }
`;

export const POST_RECIPE = gql`
  mutation postRecipe($title: String!, $ingredients: String!, $instructions: String!, $description: String!) {
    postRecipe(title: $title, ingredients: $ingredients, instructions: $instructions, description: $description) {
      _id
      title
      ingredients
      instructions
      description
      user {
        _id
      }
    }
  }
`;

export const ADD_USER = gql`
  mutation addUser(
    $firstName: String!
    $lastName: String!
    $email: String!
    $password: String!
  ) {
    addUser(
      firstName: $firstName
      lastName: $lastName
      email: $email
      password: $password
    ) {
      token
      user {
        _id
      }
    }
  }
`;
